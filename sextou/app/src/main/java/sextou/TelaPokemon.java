    private void atualizarComputador(){
        for(int i = 0; i < computador.size(); i++){
            Pokemon p = computador.get(i);
            modeloComputador.addRow(new Object[] {p.nome, p.tipo, p.nivel });
        }
    }
    private void enviarPokemon(){
        int row = jTable1.getSelectedRow();
        if(mochila.size() == 6){
            JOptionPane.showMessageDialog(this, "Limite de pokemons atingindo! ");
        }else {
        for(int i = 0; i < computador.size(); i++){
            if(row == i){
                Pokemon p = computador.get(i);
                modeloMochila.addRow(new Object[] {p.nome, p.tipo, p.nivel});
                modeloComputador.removeRow(i);
                mochila.add(computador.get(i));
                computador.remove(i);
            }
                
        }
        }
        
        
        
        
    }
    
    private void removerPokemon(){
        int row = jTable2.getSelectedRow();
        for(int i = 0; i < mochila.size(); i++){
            if(row == i){
            Pokemon p = mochila.get(i);
            modeloComputador.addRow(new Object[] {p.nome, p.tipo, p.nivel});
            modeloMochila.removeRow(i);
            computador.add(mochila.get(i));
            mochila.remove(i);
            }
            
        }
            
    }
    
    
