      public void carregarTabela(){
        // setRouwCount(0) limpa todas as linhas
        modelo.setRowCount(0);
        
        if(this.userEmail == null || this.userEmail.isBlank()) {
            JOptionPane.showMessageDialog(this, "Erro: usuario nao encontrado");
            return;
        }
        
        Usuario usuarioLogado = usuarioDao.buscarPorEmail(this.userEmail);
        
        if(usuarioLogado == null) {
                        JOptionPane.showMessageDialog(this, "Erro: usuario logado nao existente");
                        return;
        }
                
        // Chama o service
        for(Tarefas tarefa : tarefasDao.buscarTarefa(usuarioLogado.getId())){
            modelo.addRow(new Object[]{
                tarefa.getId(),               
                tarefa.getTitulo(),
                tarefa.getDescricao(),
                tarefa.getConcluido(),
            });
            System.out.println(tarefa.getConcluido());
        }
    }

