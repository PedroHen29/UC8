    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        String titulo = tituloField.getText();
        String desc = descField.getText();
        Object status = concluideField.getSelectedItem();
        String statusString = concluideField.getSelectedItem().toString();
        
        
        Usuario usuarioLogado = usuarioDao.buscarPorEmail(this.userEmail);
        int userId = usuarioLogado.getId();
        Tarefas tarefa = new Tarefas(userId, titulo, desc, statusString);
        
        if(!titulo.isBlank() || !desc.isBlank()) {
            
            tarefasService.registrar(tarefa);
            carregarTabela();
        }
        
        
        
    }//GEN-LAST:event_addBtnActionPerformed

    private void deletarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarBtnActionPerformed
        int row = taskTable.getSelectedRow();
        int id = Integer.parseInt(taskTable.getValueAt(row, 0).toString());
        
        if(id >= 0) {
            tarefasController.apagar(id);
            System.out.println("tarefa id" + id);
            carregarTabela();

        }        
          carregarTabela();

    }//GEN-LAST:event_deletarBtnActionPerformed

