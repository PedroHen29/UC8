    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int row = tabelaLivros.getSelectedRow();
        int id = Integer.parseInt(tabelaLivros.getValueAt(row, 0).toString());
        
        String titulo = tituloFild.getText().trim();
        String autor = autorFild.getText().trim();
        int ano = Integer.parseInt(anoFild.getText().trim());
        String categoria = idFild.getText().trim();
        
        if(row >= 0){
            Resultado resultado = controller.atualizar(titulo, autor, ano, categoria);
            JOptionPane.showMessageDialog(this, resultado.getMensagem(), resultado.isSucesso() ? "Sucesso" : "Erro",
                    resultado.isSucesso() ? JOptionPane.INFORMATION_MESSAGE : JOptionPane.ERROR_MESSAGE);
            carregarTabela();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

