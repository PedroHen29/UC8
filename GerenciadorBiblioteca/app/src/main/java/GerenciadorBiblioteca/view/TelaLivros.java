    public void carregarTabela(){
        // setRouwCount(0) limpa todas as linhas
        modelo.setRowCount(0);
        
        // Chama o service
        for(Livro livro : service.listarLivros()){
            modelo.addRow(new Object[]{
                livro.getId(),
                livro.getTitulo(),
                livro.getAutor(),
                livro.getAnoPublicacao(),
                livro.getCategoria()
            });
        }
    }

