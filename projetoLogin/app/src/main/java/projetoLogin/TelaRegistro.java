    private void entrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarBtnActionPerformed
        String nome = nameFild.getText();
        String email = emailFild.getText();
        char[] senha = senhaFild.getPassword();
        
        
        if(nome.trim().equals("Digite seu nome:") || email.trim().equals("Digite seu email:") || Arrays.equals(senha, new char[]{})){
        JOptionPane.showMessageDialog(this,"VocÃª deixou campos vazios! ");
    }else {
        Usuario usuario = new Usuario(nome, email, senha);
        Banco.adicionarUsuario(usuario);
        JOptionPane.showMessageDialog(this, "Usuario adicionado! ");
        }
        
    }//GEN-LAST:event_entrarBtnActionPerformed

